import Fuse from "fuse.js";

const fuse = new Fuse(dummyMembers, {
  keys: ["name", "id"],
});

export default {
  getGuild(guildID, callback, errorCallback) {
    // Gets guild information.

    if (guildID != dummyGuild.guild.id) {
      return errorCallback();
    }

    setTimeout(() => {
      callback(dummyGuild);
    }, 5000);
  },

  fetchGuildMembers(query, guildID, callback, errorCallback) {
    // Returns list of guild members based on input query/id

    if (guildID != dummyGuild.guild.id) {
      return errorCallback();
    }

    setTimeout(
      callback(
        fuse.search(query).map((v) => {
          return v.item;
        })
      ),
      3000
    );
  },
};
